import re

def validate_phone(phone_str: str) -> bool:
    """
    Проверяет валидность номера телефона.
    Номер должен состоять из 10 или 11 цифр.
    Если цифр 11, он должен начинаться с '7', '8' или '+7'.
    
    Args:
        phone_str (str): Исходная строка с номером телефона.
        
    Returns:
        bool: True, если номер валиден, иначе False.
    """
    # Очищаем строку от пробелов, тире и скобок
    cleaned_phone = re.sub(r'[\s\-\(\)]', '', phone_str)
    
    # Регулярное выражение:
    # (?:\+7|7|8) - начало на +7, 7 или 8
    # \d{10}      - ровно 10 цифр
    # Либо просто 10 цифр без кода страны
    pattern = r'^(?:\+7|7|8)?\d{10}$'
    
    return bool(re.match(pattern, cleaned_phone))
